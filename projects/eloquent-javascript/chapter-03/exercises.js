// function returns the minimum of two numbers
function min(num1, num2) {
  return num1 < num2 ? num1 : num2;
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

// function determines if a number is even using recursion.
// 0 is even, 1 is odd, N - 2 is even
function isEven(num) {
  // negative catch, use absolute value
  num = Math.abs(num);

  // even base case
  if (num === 0){
    return true;
  } else if (num === 1){  // odd base case
    return false;
  } else {
    return isEven(num - 2);
  }
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, char) {
  // make iterable array from string
  let strArr = string.split('');
  // counter variable
  let charCount = 0;

  // loop through array and increment char counter
  for (let i = 0; i < strArr.length; i++){
    if (strArr[i] === char){
      charCount++;
    }
  }
  // return char counter
  return charCount;
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

// function counts the number of 'B' characters in a string
function countBs(string) {
  // make iterable array from string
  let strArr = string.split('');
  // counter variable
  let countB = 0;

  // loop through array and count 'B's
  for (let i = 0; i < strArr.length; i++){
    if (strArr[i] === 'B'){
      countB++;
    }
  }
  // return counter
  return countB;
}

